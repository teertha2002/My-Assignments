package week1.day1;

public class FindOddNumbers {

	public static void main(String[] args) {
		 int limit = 10;

	        System.out.println("Odd numbers up to " + limit + ":");

	        for (int i = 1; i <= limit; i++) {
	            if (i % 2 != 0) {
	                System.out.print(i + " ");
	            }
	        }
	    }
	}
		

	
	       



